import java.util.*;
public class autoboxing
{
public static void main(String[] args)
{
int a=10;
Integer ob1=a;
float b=6.0f;
Float ob2=b;
String c="A@S";
String ob3=c;
boolean d=true;
Boolean ob4=d;
double e=789.04723;
Double ob5=e;
char f='u';
Character ob6=f;
System.out.println(ob1);
System.out.println(ob2);
System.out.println(ob3);
System.out.println(ob4);
System.out.println(ob5);
System.out.println(ob6);
}
}