public class unboxing
{
public static void main(String[] args)
{
Integer ob1=30;
int a=ob1;
Float ob2=7.8f;
float b=ob2;
Boolean ob3=false;
boolean c=ob3;
Double ob4=778.8767;
double d=ob4;
String ob5="A@S";
String e=ob5;
Character ob6='A';
char f=ob6;
System.out.println(a);
System.out.println(b);
System.out.println(c);
System.out.println(d);
System.out.println(e);
System.out.println(f);
}
}